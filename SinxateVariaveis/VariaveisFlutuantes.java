public class VariaveisFlutuantes {
    public static void main(String[] args) {
        double salario;
        salario = 1333.65;
        System.out.println("Meu salário é " + salario);

        double divisao = 3.14 / 2;
        System.out.println(divisao);

        int divisao2 = 5 / 2;
        System.out.println(divisao2);
        // Arredonda o resultado. Int com Int = INT
        
        double divisao3 = 5.0 / 2.0;
        System.out.println(divisao3);
        // Pelo menos um dos valores tem que estar em formato double 

    }
}
